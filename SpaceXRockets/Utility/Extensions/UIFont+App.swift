import Foundation
import UIKit

extension UIFont {
    
    /**********************************************************************************
     *   Roboto-Regular
     **********************************************************************************/
    
    class func app_RobotoRegular(size:CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Regular", size: size)!
    }
    
    /**********************************************************************************
     * Mont-HeavyDemo
     **********************************************************************************/
    
    class func app_MontHeavy(size:CGFloat) -> UIFont {
        return UIFont(name: "Mont-HeavyDEMO", size: size)!
    }
}
